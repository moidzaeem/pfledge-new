<h4>Was ist 24h-Betreuung?</h4>
Bei der 24-Stunden-Betreuung zieht eine Betreuungskraft in den Haushalt der pflegebedürftigen Person ein und übernimmt je nach Bedarf unterstützende Aufgaben, darunter:
<ul>
<li>Grundpflege (z. B. Körperhygiene, An- und Auskleiden)</li>
<li>Haushaltsführung (z. B. Kochen, Reinigen, Wäsche)</li>
<li>Begleitung & Mobilität (z. B. Spaziergänge, Arztbesuche)</li>
<li>Gesellschaft & soziale Unterstützung</li>
</ul>
Diese Form der Betreuung kann entweder von selbstständigen Betreuungskräften oder über Agenturen organisiert werden.
